enum FocusEventType {
    /**
     * 即将获得焦点
     */
    PreviewGotFocus = "PreviewGotFocus",

    /**
     * 即将失去焦点
     */
    PreviewLostFocus = "PreviewLostFocus",

    /**
     * 获得焦点
     */
    GotFocus = "GotFocus",

    /**
     * 失去焦点
     */
    LostFocus = "LostFocus"
}

export default FocusEventType;